import numpy as np

a = np.array([10,20,30])
b = np.array([1,2,3])

print("Array A:", a)
print("Array B:", b)

# Element-wise operations
print("\nAddition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)

# Scalar operations
print("\nAdd 5:", a + 5)
print("Multiply by 2:", a * 2)
